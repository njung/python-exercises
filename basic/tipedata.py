#tipe data Boolean
print(True)

#tipe data String
print("Mari belajar pemrograman Python")
print('Belajar sangat menyenangkan')

#tipe data Integer
print(20)

#tipe data Float
print(3.14)

#tipe data Hexadecimal
print('1d3')

#tipe data Complex
print(5j)

#tipe data List
print([1,2,3,4,5])
print(["satu", "dua", "tiga", "empat", "lima"])

#tipe data Tuple
print((1,2,3))
print(("satu", "dua", "tiga"))

#tipe data Dictionary
print({"nama":"Misca", 'umur':25})
#tipe data Dictionary dimasukkan ke dalam variabel biodata
biodata = {"nama":"Jono", 'umur':17} #proses inisialisasi variabel biodata
print(biodata) #proses pencetakan variabel biodata
type(biodata) #fungsi untuk mengecek jenis tipe data
